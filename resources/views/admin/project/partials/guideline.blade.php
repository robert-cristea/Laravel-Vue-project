        <div class="guideline-pill-content">
                Data Sources and Record Keeping
        </div>
    </div>
    <div class="col-9 guideline-pill-white">
        <div class="guideline-pill">
            Concessionaire have to keep recording of their historical data  on energy, water consumption, paper, materials on site usage/ transportation, fuel consumption, tree plantation and waste monthly and annuallly. The data need to be provide in 3 stages of highway namely design stage, construction stage and operation/maintenance stage. For design stage, design consultant need to provide data consumption on electrical, water, paper and transportation for office usage only during the highway design period. In construction stage, highway concessionaire need to provide activity data which emitted CO2 during construction stage. The activity divides into 3 scopes:
            <ul>
                <li>Scope 1: (Direct emission involves in machinery combustion onsite)</li>
                <li>Scope 2: (Indirect emission involves in electricity consumption and water consumption)</li>
                <li>Scope 3: (Embodied carbon involves in material usage, material transportation to construction site and paper used at site office)</li>
            </ul>
            Lastly in operation and maintenance stage, the data requirement involves in activity operate for highway maintenance such as electrical consumption, water consumption, transportation, waste material, paper and trees plantation.

