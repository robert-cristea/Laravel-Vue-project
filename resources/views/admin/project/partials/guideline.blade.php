            <li>Lembaga Lebuhraya Malaysia (LLM) is the highways agency served under government and is committed to contributing the Government's target for greenhouse gas (GHG) emissions reductions.</li>
            <li>The first project was originally initiated on 2012, whereby during the time the introduction of Malaysia Green Highway Index (MyGHI) was introduced which concerning in depth on the scope of points for sustainable practices and policies.</li>
            <li>The five main criteria cover mainly in sustainable design and construction; energy efficiency; environmental and water management; material and technology; and social and safety.</li>
            <li>After several discussions with LLM, Concessionaire and UTM, LLM would like to extend their study to a holistic assessment of the impact of Malaysian Highway on the environment, ranging from site clearing, supply activities, administration and interaction with its stakeholders and also during its operation.</li>
            <li>Align with The National Green Technology Policy greenhouse gases (GHGs) emission was introduced in order to ensure sustainable development approach can significantly progress besides providing the major improvement, especially in the transportation sector. Carbon footprint is commonly used to describe the total amount of Carbon Dioxide (CO2) and other greenhouse gas (GHG) emissions for which an individual and an organisation is responsible. Besides that it can be defined as total amount of CO2 and GHGs (direct and indirect) emitted over the full cycle of a road. The life cycle includes construction, operation, and maintenance phases. Its “life” ends when the road needs to be completely reconstructed or when it is abandoned.</li>
        </ol>
