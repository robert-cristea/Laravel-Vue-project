            Why we need Carbon Footprint Calculator?
        </div>
    </div>
    <div class="col-9 guideline-pill-white">
        This Carbon Calculator Tool was developed to enable the Highway Concessionaire to identify their carbon footprint associate with four phases of Highway’s life cycle namely Design, Construction, Operation and Maintenance stage. The tool provides a baseline reporting from the reported period calculation of carbon and GHG emissions as part of a Carbon Reduction Initiative. The users need to provide the required input data for each work-book sheet in order to calculate GHG emissions from different aspects such as electrical, water, transportation, waste product / material, papers used, and plantation of trees. Inside the calculator sheet, Design & Construction stages are combined in one folder together and same goes to Operation & Maintenance stages. By using this calculator, the user can see the result of both direct and indirect emissions and GHG produced.
